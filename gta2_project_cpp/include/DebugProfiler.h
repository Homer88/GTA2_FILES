#pragma once

#include <cstdint>
#include <cstddef>

namespace gta2 {

/**
 * @brief Структура отладочного профилировщика (оригинальное имя: Mike).
 * 
 * Глобальный объект используется для тестирования, отладки и профилирования
 * графической подсистемы и других компонентов игры.
 * Адрес глобального экземпляра: 0x5E8168
 * Размер: 1048 байт (0x418)
 */
struct DebugProfiler {
    // Смещение 0x00 - 0x417: Внутренние данные профилировщика
    // Включает флаги, буферы для строк статистики ("DisplayDraw", "Total", "Process"),
    // счетчики кадров, время выполнения и т.д.
    // Точная карта полей требует дальнейшего реверс-инжиниринга,
    // поэтому пока представляем как сырой буфер.
    uint8_t data[1048];

    /**
     * @brief Конструктор (оригинал: 0x46B040).
     * Инициализирует структуру значениями по умолчанию.
     * Размер кода: 112 байт.
     */
    DebugProfiler();

    /**
     * @brief Метод сброса или простой проверки состояния (оригинал: 0x474100).
     * Размер кода: 15 байт.
     */
    void ResetOrCheck();

    /**
     * @brief Основной метод профилирования/отрисовки статистики (оригинал: 0x474530).
     * Работает со строками отладки ("do_mike", "DisplayDraw" и др.).
     * Размер кода: 1580 байт.
     */
    void RenderDebugStats();

    /**
     * @brief Получение адреса глобального экземпляра.
     * В реальной игре это жестко заданный адрес, здесь эмулируем через статический объект.
     */
    static DebugProfiler& Instance();
};

// Проверка размера структуры
static_assert(sizeof(DebugProfiler) == 1048, "Size of DebugProfiler must be 1048 bytes");

} // namespace gta2
