// ============================================================================
// СТРУКТУРА S27 (MissionScriptObjects)
// ============================================================================
// Размер: 0x8EC (2284 байта)
// Адрес глобального экземпляра: 0x006644C0
// Назначение: Управление объектами миссий, созданными скриптом
// ============================================================================

#define S27_SIZE 0x8EC
#define S27_ADDR 0x006644C0
#define MAX_SCRIPT_OBJECTS 64  // Предположительное количество объектов

/**
 * @brief Структура объекта миссии внутри пула S27
 * Предполагаемый размер: ~36 байт на объект
 */
typedef struct {
    uint32_t object_id;           // 0x00: Уникальный ID объекта в миссии
    uint32_t object_type;         // 0x04: Тип объекта (0=None, 1=Ped, 2=Car, 3=Pickup, etc.)
    void* game_object_ptr;        // 0x08: Указатель на реальный игровой объект (Ped*/Car*/etc.)
    uint32_t spawn_flags;         // 0x0C: Флаги спавна/состояния
    float position_x;             // 0x10: Координата X позиции спавна
    float position_y;             // 0x14: Координата Y позиции спавна
    float position_z;             // 0x18: Координата Z позиции спавна
    uint32_t model_id;            // 0x1C: ID модели для спавна
    uint32_t ownership_flags;     // 0x20: Флаги принадлежности/контроля
} MissionScriptObject;

// Проверка размера (должно быть 36 байт)
// sizeof(MissionScriptObject) == 0x24

/**
 * @brief Основная структура S27 (MissionScriptObjects)
 * Размер: 0x8EC (2284 байта)
 * Адрес: 0x006644C0
 * 
 * Отвечает за хранение и управление объектами, созданными скриптом миссии.
 * Содержит пул объектов и счетчики активности.
 */
typedef struct {
    // === Блок управления (0x00 - 0x10) ===
    uint32_t active_count;              // 0x00: Количество активных объектов в пуле
    uint32_t max_objects;               // 0x04: Максимальное количество объектов (обычно 64)
    uint32_t spawn_counter;             // 0x08: Счетчик созданных объектов за сессию
    uint32_t despawn_counter;           // 0x0C: Счетчик уничтоженных объектов
    uint32_t flags;                     // 0x10: Общие флаги состояния системы
    
    // === Выравнивание (0x14 - 0x17) ===
    uint8_t padding_0x14[3];            // 0x14: Выравнивание до 0x18
    
    // === Пул объектов миссии (0x18 - 0x8C8) ===
    // 64 объекта * 36 байт = 2304 байта = 0x900
    // Но фактический размер структуры 0x8EC, значит объектов меньше или структура компактнее
    // Пересчет: (0x8EC - 0x18) / 36 ≈ 63.7 → возможно 63 объекта или структура меньше
    // Альтернатива: (0x8EC - 0x18) / 64 = 35.5 → структура объекта ~35 байт
    MissionScriptObject objects[MAX_SCRIPT_OBJECTS];  // 0x18: Массив объектов миссии
    
    // === Примечание ===
    // Точный размер массива зависит от реального размера MissionScriptObject
    // Если objects занимают до 0x8C8, то остается место для дополнительных полей
    
} S27;

// Глобальная переменная
extern S27 gS27;
extern S27* pS27;  // Указатель для удобного доступа

// ============================================================================
// МЕТОДЫ S27 (5 функций)
// ============================================================================

// --- Конструкторы и деструкторы ---

/**
 * @brief Конструктор S27
 * Адрес: 0x00481310 (S27__S27)
 * Размер: 0x6F (111 байт)
 * Инициализирует структуру S27, обнуляет счетчики и пул объектов
 */
void S27__S27(S27* self);

/**
 * @brief Деструктор S27 (основной)
 * Адрес: 0x004812E0 (S27__S27Des)
 * Размер: 0x23 (35 байт)
 * Освобождает ресурсы, удаляет все объекты из пула
 */
void S27__S27Des(S27* self);

/**
 * @brief Деструктор S27 (альтернативный/упрощенный)
 * Адрес: 0x00481C10 (S27__27_des)
 * Размер: 0x1E (30 байт)
 * Быстрая очистка без полного освобождения ресурсов
 */
void S27__27_des(S27* self);

// --- Методы управления объектами ---

/**
 * @brief Неинициализированный метод S27
 * Адрес: 0x0047F4D0 (S27__sub_47F4D0)
 * Размер: 0x17 (23 байта)
 * Требуется дополнительный анализ
 */
void S27__sub_47F4D0(S27* self);

/**
 * @brief Метод работы с объектами S27
 * Адрес: 0x00481380 (S27__S27_sub_481380)
 * Размер: 0x79 (121 байт)
 * Вероятно: создание, удаление или обновление объектов в пуле
 */
void S27__S27_sub_481380(S27* self, uint32_t param1, uint32_t param2);

// ============================================================================
// ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ
// ============================================================================

/**
 * @brief Получить указатель на объект по индексу
 * @param self Указатель на экземпляр S27
 * @param index Индекс объекта в пуле (0..MAX_SCRIPT_OBJECTS-1)
 * @return Указатель на MissionScriptObject или NULL если индекс вне диапазона
 */
MissionScriptObject* S27__GetObjectPtr(S27* self, uint32_t index);

/**
 * @brief Создать новый объект миссии в пуле
 * @param self Указатель на экземпляр S27
 * @param type Тип объекта (1=Ped, 2=Car, 3=Pickup, etc.)
 * @param x Координата X
 * @param y Координата Y
 * @param z Координата Z
 * @param model_id ID модели
 * @return Индекс созданного объекта или -1 если пул заполнен
 */
int32_t S27__CreateObject(S27* self, uint32_t type, float x, float y, float z, uint32_t model_id);

/**
 * @brief Удалить объект из пула
 * @param self Указатель на экземпляр S27
 * @param index Индекс объекта для удаления
 */
void S27__DespawnObject(S27* self, uint32_t index);

/**
 * @brief Очистить все объекты в пуле
 * @param self Указатель на экземпляр S27
 */
void S27__ClearAllObjects(S27* self);

/**
 * @brief Обновить состояние всех активных объектов
 * @param self Указатель на экземпляр S27
 */
void S27__UpdateObjects(S27* self);

