#ifndef GTA2_TYPES_H
#define GTA2_TYPES_H

#include <stdint.h>
#include <stdbool.h>

// Basic types matching GTA2 binary
typedef uint8_t u8;
typedef int8_t s8;
typedef uint16_t u16;
typedef int16_t s16;
typedef uint32_t u32;
typedef int32_t s32;
typedef float f32;
typedef double f64;

#endif // GTA2_TYPES_H
