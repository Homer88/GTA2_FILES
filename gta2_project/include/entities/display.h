#ifndef DISPLAY_H
#define DISPLAY_H

#include "../types.h"
#include "../forward_declarations.h"

// ============================================================================
// СИСТЕМА ОТОБРАЖЕНИЯ GTA2 (Display)
// ============================================================================
//
// Display (S18, размер ~0x1C байт) - Менеджер отображения спрайтов/объектов
// 
// Структура управляет коллекцией из 7 контейнеров (S19), каждый из которых
// хранит указатели на объекты для отрисовки (SpriteS1, Ped, Vehicle и т.д.)
//
// Основные функции:
//   - Display::Display()         : Конструктор (0x44B8F0)
//   - Display::~Display()        : Деструктор (0x45A9B0)
//   - Display::ClearAll()        : Очистка всех контейнеров (0x44B890)
//   - Display::ClearIndex()      : Очистка конкретного индекса (0x44B8B0)
//   - Display::AddObject()       : Добавление объекта (0x44B990)
//   - Display::RemoveAll()       : Удаление всех объектов (0x44B9E0)
//   - Display::GetTimestamp()    : Получение временной метки (0x4C3950)
//
// Глобальная переменная: gDisplay (адрес: 0x5E8B80)
// ============================================================================

// ----------------------------------------------------------------------------
// S19: Контейнер объектов отображения
// ----------------------------------------------------------------------------
// Адрес в IDA: используется внутри Display
// Размер: 0x4 (4 байта)
// Назначение: Обёртка над указателем на объект (SpriteS1*, Ped*, etc.)
// ----------------------------------------------------------------------------
struct S19 {
    void* pObject;        // 0x00 - Указатель на отображаемый объект
    
    // Методы S19 (IDA адреса):
    //   S19::S19()           : Конструктор (0x4C39B0)
    //   S19::~S19()          : Деструктор (0x44B970)
    //   S19::Clear()         : Очистка (0x4C4D60)
    //   S19::Reset()         : Сброс (0x4C4D20)
    //   S19::SetObject()     : Установка объекта (0x4C4BF0)
};

// Проверка размера: sizeof(S19) == 4
_Static_assert(sizeof(S19) == 4, "S19 must be 4 bytes");

// ----------------------------------------------------------------------------
// S18: Display - Менеджер отображения
// ----------------------------------------------------------------------------
// Адрес в IDA: 0x5E8B80 (глобальный экземпляр gDisplay)
// Размер: 0x1C (28 байт) = 7 * sizeof(S19) + служебные данные
// ----------------------------------------------------------------------------
struct Display {
    // === Массив контейнеров объектов (0x00 - 0x1B) ===
    struct S19 SpriteContainers[7];  // 0x00 - 7 контейнеров для разных типов спрайтов
                                     //   Индекс 0: Игрок
                                     //   Индекс 1: Пешеходы (Peds)
                                     //   Индекс 2: Транспорт (Vehicles)
                                     //   Индекс 3: Объекты (Objects)
                                     //   Индекс 4: Эффекты (Particles)
                                     //   Индекс 5: UI элементы
                                     //   Индекс 6: Резерв
    
    // Методы Display (IDA адреса):
    // ------------------------------------------------------------------------
    // void Display::ClearAll()
    //   Адрес: 0x44B890
    //   Описание: Очищает все 7 контейнеров вызовом S19::Clear()
    //   Используется: При сбросе игры, загрузке уровня
    // ------------------------------------------------------------------------
    // void Display::ClearIndex(int index)
    //   Адрес: 0x44B8B0
    //   Описание: Очищает контейнер по индексу (0-6)
    //   Параметры: index - индекс контейнера
    //   Возвращает: количество очищенных объектов
    // ------------------------------------------------------------------------
    // int Display::AddObject(void** pObj)
    //   Адрес: 0x44B990
    //   Описание: Добавляет объект в соответствующий контейнер
    //   Параметры: pObj - указатель на указатель объекта (SpriteS1*, Ped*, etc.)
    //   Возвращает: количество добавленных объектов
    // ------------------------------------------------------------------------
    // void Display::RemoveAll()
    //   Адрес: 0x44B9E0
    //   Описание: Удаляет все объекты из всех контейнеров с освобождением памяти
    //   Используется: В деструкторе, при выгрузке уровня
    // ------------------------------------------------------------------------
    // u64 Display::GetTimestamp()
    //   Адрес: 0x4C3950
    //   Описание: Получает текущую временную метку системы
    //   Возвращает: timestamp (u64)
    // ------------------------------------------------------------------------
    // Display* Display::~Display(char bFreeMemory)
    //   Адрес: 0x45A9B0
    //   Описание: Деструктор
    //   Параметры: bFreeMemory - если 1, освобождает память структуры
    // ------------------------------------------------------------------------
};

// Проверка размера: sizeof(Display) >= 28
// Примечание: Точный размер требует уточнения через IDA

// ----------------------------------------------------------------------------
// Глобальные переменные
// ----------------------------------------------------------------------------

// Главный экземпляр Display
// Адрес в IDA: 0x5E8B80
extern struct Display* gDisplay;

// Счётчики производительности (обновляются в ClearIndex)
// Адрес в IDA: 0x5E8B7C
extern u32 g_DisplayClearCount;

// Адрес в IDA: 0x5E8B78
extern u32 g_DisplayAddCount;

// ----------------------------------------------------------------------------
// Вспомогательные функции (не являются методами класса)
// ----------------------------------------------------------------------------

// Показ текста на экране (форматированный вывод)
// Адрес в IDA: 0x44BA00区域
// Параметры:
//   pTextWchar - текст в формате wchar_t
//   pFormat    - формат строки (char*)
//   ...        - аргументы формата
// Пример: ShowTextDisplay(&text, "FPS: %d", fps);
int ShowTextDisplay(wchar_t* pTextWchar, const char* pFormat, ...);

// Ошибка отображения (для отладки)
// Адрес в IDA: 0x45AA50区域
int DisplayError(int errorCode, char* pFilename, int lineNumber);

#endif // DISPLAY_H
