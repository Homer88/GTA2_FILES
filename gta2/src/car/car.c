/**
 * @brief Конструктор CarsPrefabs.
 * 
 * Оригинальный адрес: 0x004254F0
 * Логика из листинга:
 * 1. Вызов Construct для массива sCarArr306 (306 элементов по 0xBC байт).
 * 2. Построение связного списка свободных элементов через поле LastCar (pNext).
 *    - Цикл проходит с конца массива к началу (или наоборот, связывая элементы).
 *    - asm: *p_LastCar = (Car *)(p_LastCar + 28); где 28 = 0x1C = 28 * 4 байта? 
 *      НЕТ: 0xBC (188) / 4 = 47. Шаг указателя в цикле: p_LastCar += 47 (int*).
 *      47 * 4 = 188 байт. То есть переход к следующему элементу массива.
 * 3. Установка последнего элемента в NULL (конец списка).
 * 4. Инициализация счетчиков в 0.
 */
CarsPrefabs* CarsPrefabs_Constructor(CarsPrefabs* self) {
    if (!self) return NULL;

    CCar* sCar = self->carPool;
    
    // 1. Инициализация каждого элемента массива (вызов конструктора CCar)
    for (int i = 0; i < 306; ++i) {
        CCar_Constructor(&sCar[i]);
    }

    // 2. Построение свободного списка (Free List)
    // В оригинале: p_LastCar = &sCar->LastCar (смещение 0x4C внутри элемента)
    // Цикл связывает элементы: текущий->pNext = следующий
    
    // Эмуляция логики ассемблера:
    // do { *p_LastCar = (Car *)(p_LastCar + 47); p_LastCar += 47; } while(--IndexCar);
    // Это означает: Element[i].pNext = &Element[i+1]
    
    for (int i = 0; i < 305; ++i) {
        sCar[i].pNext = &sCar[i + 1];
    }
    
    // Последний элемент указывает на NULL
    sCar[305].pNext = NULL;

    // 3. Инициализация заголовков списков
    self->pFreeListHead = sCar;       // Голова свободных - первый элемент
    self->pActiveListHead = NULL;     // Голова активных - пуста
    self->carsCount = 0;              // Счетчик активных = 0

    return self;
}
